# Si uno quiere añadir comillas a un texto se hace con lo siguiente ya que vs code detecta comillas como codigo.
# Se pone comillas dobles dentro de comillas simples
nombre_del_dueño = '"DuskStar"'
print(nombre_del_dueño)
# Tambien se puede viceversa para poner comillas simples dentro de comillas dobles
nombre_del_dueño = "'DuskStar'"
print(nombre_del_dueño)
# Si queremos varios caracteres tomados como texto en un string es con backslash
nombre_del_dueño = '"Dusk""Star"'
print(nombre_del_dueño)
